<h2>1 CHE COS’E’ PLAUNAZINE E A COSA SERVE</h2><br>
PLAUNAZINE contiene due sostanze chiamate olmesartan medoxomil e idrocloratiazide. Entrambe servono
a controllare la pressione arteriosa alta (ipertensione).<br>
- L’olmertasan medoxomil appartiene ad un gruppo di medicinali chiamati “antagonisti recettoriali
