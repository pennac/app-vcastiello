- L’idroclortiazide appartiene ad un gruppo di sostanze chiamate “diuretici tiazidici”. Essa riduce la
pressione arteriosa aiutando l’organismo ad eliminare i liquidi in eccesso facendo si che i reni
producano più urina.<br>
Le verrà somministrato PLAUNAZINE se PLAUNAC (olmesartan medoxomil) da solo non ha controllato
adeguatamente la pressione arteriosa. Quando vengono somministrate insieme, i due principi attivi di
PLAUNAZIDE aiutano a ridurre la pressione arteriosa più di quanto non facciano se somministrati da soli.<br>
Se già sta assumendo medicinali per trattare la pressione alta, il suo medico potrebbe somministrarle
PLAUNAZIDE per ottenere un’ulteriore riduzione.<br>
La pressione arteriosa alta può essere controllata con medicinali come PLAUNAZIDE compresse. Il suo
